var calculator = {}

calculator.add = function(num1,num2){
return num1 + num2;
}

calculator.subtract = function(num1,num2){
return num1 - num2;
}

calculator.mutiply = function(num1,num2){
return num1 * num2;
}

calculator.divide = function(num1,num2){
return num1 / num2;
}

calculator.exponents = function(num1,num2){
return Math.pow(num1,num2);
}
