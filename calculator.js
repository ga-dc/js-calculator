var calculator = {
  addition: function(a,b) {
    return a + b;
  },
  subtraction: function (a,b)  {
    return a - b;
  },
  multiplication: function (a,b) {
    return a * b;
  },
  division: function (a,b) {
    return a / b;
  }
};
console.log(calc.addition(4,5));
console.log(calc.subtraction(4,5));
console.log(calc.multiplication(4,5));
console.log(calc.division(4,5));
