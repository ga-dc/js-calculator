var add = function (x, y)  {
  return (x + y);
};
var subtract = function (x, y)  {
  return (x - y);
};
var multiply = function (x, y) {
    return (x * y);
};
var divide = function (x, y)  {
  return (x/y);
};
var exponent = function(x,y) {
  return Math.pow(x,y);
};
var root = function(x) {
  return Math.sqrt(x);
};
