var calculator = {
  add: function(x,y) {
    return x + y;
  },

  subtract: function(x,y) {
    return y - x;
  },

  multiply: function(x,y) {
    return x * y;
  },

  divide: function(x,y) {
    return (x/y);
  },

  exponent: function(x,y) {
    return Math.pow(x,y);
  },

  square: function(x) {
    return (x*x);
  }
};
