var calculator = {

  add: function(a,b){
    return a+b;
  },
  Subtraction: function(a,b){
    return a-b;
  },
  Multiplication: function(a,b){
    return a*b;
  },
  division: function(a,b){
    return a/b;
  },
  exponents: function(a,b){
    return Math.pow(a,b)
  },
  square: function(a){
    return a*a;
  }
}
